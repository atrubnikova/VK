package domain;

public class Favorite {
    private int count;
    private int userLikes;

    public void setCount() {
        this.count = count;
    }

    public int getCount() {
        return count;
    }

    public void setUserLikes(int userLikes) {
        this.userLikes = userLikes;
    }

    public int getUserLikes() {
        return userLikes;
    }
}
